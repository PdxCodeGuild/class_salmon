# + does addition
# - does subtraction
# / does division
# * does multiplication
# % does whole division but shows remainder, modulus
# < left thing is less than right thing
# > left thing is greater than right thing
# <= left thing is less than or equal to the right thing
# >= left thing is greater than or equal to the right thing

# Print statement
